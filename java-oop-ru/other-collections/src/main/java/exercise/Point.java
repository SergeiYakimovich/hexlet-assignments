package exercise;

// BEGIN
public class Point {
    private int x;
    private int y;

    public Point(int value1, int value2) {
        this.x = value1;
        this.y = value2;
    }

    public Point() {
    }

    public void setX(int value) {
        this.x = value;
    }

    public void setY(int value) {
        this.y = value;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
// END
